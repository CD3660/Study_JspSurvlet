package controller;

public class CDVO {
    private String cd1, cd2, cd3;

    public CDVO(String cd1, String cd2, String cd3) {
        this.cd1 = cd1;
        this.cd2 = cd2;
        this.cd3 = cd3;
    }

    public String getCd1() {
        return cd1;
    }

    public void setCd1(String cd1) {
        this.cd1 = cd1;
    }

    public String getCd2() {
        return cd2;
    }

    public void setCd2(String cd2) {
        this.cd2 = cd2;
    }

    public String getCd3() {
        return cd3;
    }

    public void setCd3(String cd3) {
        this.cd3 = cd3;
    }
}
